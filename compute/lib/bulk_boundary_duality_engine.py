# 10. CONFORMAL WEIGHT SPECTRUM
# =========================================================================

def conformal_weight_spectrum(pair: KoszulDualPair,
                               max_level: int = 5) -> Dict[str, Any]:
    """Compute the conformal weight spectrum of line operators.

    For each family, the line operators have a spectrum of conformal
    weights that depends on the Koszul dual representation theory.

    For Heisenberg: h(p) = p^2/(2k), continuous spectrum.
    For Virasoro: h arbitrary (continuous) at generic c;
                  discrete spectrum at rational c (BPZ).
    For affine: h(j) = j(j+1)/(k+2), discrete at integer k.
    """
    name = pair.name
    weights = {}

    if name == 'Heisenberg':
        k = Symbol('k')
        for p_val in range(-max_level, max_level + 1):
            weights[f'p={p_val}'] = Rational(p_val**2, 2)  # k=1 normalization

    elif name == 'Virasoro':
        c = Symbol('c')
        # BPZ table for c = 1/2 (Ising model): h_{1,1}=0, h_{2,1}=1/2, h_{1,2}=1/16
        # These are SPECIFIC to rational c, not general
        weights['vacuum'] = S.Zero
        weights['generic_h'] = Symbol('h')

    elif name == 'Affine sl_2':
        k = Symbol('k')
        for j in range(max_level + 1):
            j_sym = Rational(j, 2) if j % 2 == 1 else Rational(j, 2)
            weights[f'j={j_sym}'] = j_sym * (j_sym + 1) / (k + 2)

    elif name == 'W_3':
        # W_3 has a 2-parameter family of highest-weight modules
        weights['vacuum'] = S.Zero
        weights['generic'] = Symbol('h_W3')

    return {
        'algebra': name,
        'spectrum': weights,
        'continuous': name in ('Heisenberg', 'Virasoro'),
        'discrete_at_integer_level': name == 'Affine sl_2',
    }


