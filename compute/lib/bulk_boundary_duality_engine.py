# 2. KAPPA COMPLEMENTARITY VERIFICATION (THEOREM C)
# =========================================================================

def verify_kappa_complementarity(pair: KoszulDualPair) -> Dict[str, Any]:
    """Verify kappa(A) + kappa(A!) = rho*K (Theorem C).

    For Kac-Moody and free fields: rho*K = 0.
    For W-algebras: rho*K = constant depending only on the algebra type.

    The complementarity constant is LEVEL-INDEPENDENT. This is the
    content of Theorem C: the modular characteristic is duality-constrained.
    """
    kappa_sum = simplify(pair.kappa + pair.dual_kappa)
    expected_sum = pair.kappa_sum

    # Check level-independence
    k = Symbol('k')
    c = Symbol('c')

    sum_has_k = kappa_sum.has(k)
    sum_has_c = kappa_sum.has(c)

    return {
        'algebra': pair.name,
        'kappa': str(pair.kappa),
        'dual_kappa': str(pair.dual_kappa),
        'kappa_sum': str(kappa_sum),
        'expected_sum': str(expected_sum),
        'match': simplify(kappa_sum - expected_sum) == 0,
        'level_independent': not sum_has_k and not sum_has_c,
        'complementarity_constant': str(expected_sum),
    }


