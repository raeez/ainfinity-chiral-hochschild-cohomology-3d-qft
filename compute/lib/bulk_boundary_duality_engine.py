def cross_volume_shadow_class_bridge(pair: KoszulDualPair) -> Dict[str, Any]:
    """Verify that shadow archetype classification agrees across volumes.

    Vol I classifies algebras by shadow depth:
    G (Gaussian, depth 2), L (Lie/tree, depth 3),
    C (Contact/quartic, depth 4), M (Mixed, infinite depth).

    Vol II should arrive at the same classification via the SC bar complex.
    """
    # Shadow class from Vol I data
    vol1_class = pair.shadow_class

    # Expected from pole orders
    max_pole = max(pair.pole_orders.values()) if pair.pole_orders else -1

    # The pole order constrains the shadow depth:
    # pole 0 or 1 => G or L (depth 2 or 3)
    # pole 3 => M (Virasoro)
    # pole 5 => M (W_3)

    expected_class = 'unknown'
    if max_pole <= 0:
        expected_class = 'G or C'
    elif max_pole == 1:
        if len(pair.algebra_generators) == 1:
            expected_class = 'G'
        else:
            expected_class = 'L'
    elif max_pole >= 3:
        expected_class = 'M'

    return {
        'algebra': pair.name,
        'vol1_shadow_class': vol1_class,
        'vol2_pole_order': max_pole,
        'expected_class': expected_class,
        'consistent': vol1_class in expected_class or vol1_class == expected_class,
    }


